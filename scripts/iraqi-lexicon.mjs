export function adaptEnglish(en) {
  return EN_ADAPT[en] ?? en;
}

export function msaToIraqi(text) {
  if (!text || !/[\u0600-\u06FF]/.test(text)) return text;
  let t = text;
  for (const [from, to] of IRAQI_SWAP) {
    t = t.split(from).join(to);
  }
  return t;
}

