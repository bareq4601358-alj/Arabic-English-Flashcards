export function stripLatinParens(ar) {
  return String(ar)
    .replace(/\s*\([^)]*[A-Za-z][^)]*\)/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

export function iraqiFor(en, msaFallback = "") {
  const key = adaptEnglish(en);
  if (IRAQI_BY_EN[key]) return IRAQI_BY_EN[key];
  if (IRAQI_BY_EN[en]) return IRAQI_BY_EN[en];
  if (IRAQI_PARTICLES[key.toLowerCase()]) return IRAQI_PARTICLES[key.toLowerCase()];
  const low = key.toLowerCase();
  if (IRAQI_PARTICLES[low]) return IRAQI_PARTICLES[low];
  if (msaFallback) return msaToIraqi(msaFallback);
  return msaFallback || key;
}
